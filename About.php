  <!-- START OBJECTIVES OF PESO -->
  <section>
    <h2 text align="center"><span><b>OBJECTIVES OF PESO?</b></span></h2>
    <div><br>
      <p text align="center"><b>General Objective:</b></p>
      <ul text align="center">Ensure the prompt, timely and efficient delivery of employment service and provision of information on the other DOLE programs.</ul>
      <p text align="center"><b>Specific Objectives:</b></p>
      <ul text align="center">Provide a venue where people could explore simultaneously various employment options and actually seek assistance they prefer.</ul>
      <ul text align="center">Serve as referral and information center for the various services and programs of DOLE and other government agencies present in the area.</ul>
      <ul text align="center">Provide clients with adequate information on employment and labor market situation in the area.</ul>
      <ul text align="center">Network with other PESOs within the region on employment for job exchange purposes.</ul>
    </div>
  </section>
  <!-- END OBJECTIVES OF PESO -->
  <br><br>
  <!-- START FUNCTIONS OF PESO -->
  <section >
    <h2 text align="center"><span><b>FUNCTIONS OF THE PESO?</b></span></h2>
    <div class="text"> <br>
      <ul>
        <li align="justify">Encourage employers to submit to the PESO on a regular basis a list of job vacancies in their respective establishments in order to facilitate the exchange of labor market information services to job seekers and employers by providing employment services to job seeker, both for local and overseas employment, and recruitment assistance to employers;</li>
        <li align="justify">Develop and administer testing and evaluation instruments for effective job selection, training and counseling;</li>
        <li align="justify">Provide persons with entrepreneurship qualities access to the various livelihood and self-employment programs offered by both government and non-governmental organizations at the provincial/city/municipal/barangay levels by undertaking referrals for such programs;</li>
        <li align="justify">Undertake employability enhancement trainings/seminar for jobseekers as well as those would like to change career or enhance their employability. This function is presently supervised by TESDA and conducted by other training;</li>
        <li align="justify">Provide employment and occupational counseling, career guidance, mass motivation and values development activities;</li>
        <li align="justify">Conduct pre-employment counseling and orientation to prospective local and overseas workers;</li>
        <li align="justify">Provide reintegration assistance services to returning Filipino migrant workers: and</li>
        <li align="justify">Perform such functions as willfully carry out the objectives of this Act. </li>
      </ul>
    </div>
  </section>
  <!-- END FUNCTIONS OF PESO -->
  <br><br>
  <!-- START SPECIAL SERVICE-->
  <section >
    <h2 text align="center"><span><b>SPECIAL SERVICES OF PESO?</b></span></h2>
    <div class="text"><br>
      <ul>
        <li align="justify">Jobs Fairs - these shall be conducted periodically all over the country to bring together in one venue job seekers and employers for immediate matching;</li>
        <li align="justify">Livelihood and Self-employment Bazaars - these will give clients information on the array of livelihood programs they choose to avail of, particularly in the rural areas;</li>
        <li align="justify">Special Credit Assistance for Placed Overseas Workers - this type of assistance will enable poor but qualified applicants to avail of opportunities for overseas employment;</li>
        <li align="justify">Special Program for Employment of Students and Out-of-School Youth (SPESOS) - this program shall endeavor to provide employment to deserving students and out-of-school youths and out-of-school youths coming from poor families during summer and/or Christmas vacations as provided for under Republic Act No. 7323 and its implementing rules, to enable them to pursue their education;</li>
        <li align="justify">Work Appreciation Program (WAP) - this program aims to develop the values of work appreciation and ethics by exposing the young to actual work situations;</li>
        <li align="justify">Workers Hiring for Infrastructure Projects (WHIP) - this program is in pursuance of Republic Act No. 6685 which requires construction companies, including the Department of Public Works and Highways and contractor for government-funded infrastructure projects, to hire thirty percent (30%) of skilled and fifty percent (50%) of unskilled labor requirements from the areas where the project is constructed/located; and</li>
        <li align="justify">Other programs/activities developed by DOLE to enhance provision of employment assistance to PESO clients, particularly for special groups of disadvantaged workers such as persons with disabilities (PWDs) and displaced workers.</li>
      </ul>
    </div>
  </section>
